                    </tr>
                    <tr>
                        <td>Yes, <strong>there is</strong>.</td>
                        <td>No, <strong>there isn’t</strong>.</td>
                    </tr>
                    <tr>
                        <td colspan="
                        2"><strong>There isn’t</strong> a good chance I can have a great trip.</td>
                    </tr>
                    <tr>
                        <td colspan="
                        2"><strong>There are</strong> many great shops in Montreal.</td>
                    </tr>
                    <tr>
