        <h3 class="sub-heading">3.6 Present Simple and Adverbs/ Expressions of Frequency</h3>
        <p class="paragraph-tex">O Present Simple é utilizado quando se faz afirmações referentes a algo vivido no presente, tratando sempre de hábitos, rotinas e ações que ocorrem com regularidade . Acompanhe exemplos desses usos na conversa de Rosa e Carly:</p>
        <ul>
            <li>Well, I don’t speak French fluently, but I <strong>usually get by with</strong> a dictionary.</li>
            <li>People <strong>often try</strong> to help you when they see you’re trying to communicate in their language.</li>
            <li>And I <strong>always hear</strong> people say that the Canadian people are very nice and friendly, so I think there’s a good chance I can have a great trip.</li>
        </ul>
        <p class="paragraph-tex">Quando Rosa utiliza usually, often e always, ela está se referindo a ações que ocorrem com regularidade através do uso de advérbios de frequência.</p>
        <p class="paragraph-tex">Em frases com o verb To be, os adverbs of frequency são posicionados depois de am, is e are:</p>
        <ul>
            <li>They’<strong>re always</strong> friendly at that travel agency.</li>
            <li>I’<strong>m usually</strong> excited before an air trip.</li>
        </ul>
        <p class="paragraph-tex">Além dos adverbs of frequency, podemos usar outras expressões que indicam a quantidade de vezes que algo é ou foi realizado. Lembre-se que estas expressions of frequency podem vir no início ou no final da frase. Observe:</p>
        <ul>
            <li>They travel to see their friends <strong>twice a</strong> year.</li>
            <li>She takes a flight <strong>every week</strong>.</li>
            <li>We pack our bags <strong>every night</strong>.</li>
        </ul>
        <p class="paragraph-tex">Utilizamos o Present Simple associado aos adverbs e as expressions of frequency. Por este motivo é importante relembrar o uso deste tempo verbal nas formas afirmativa, negativa e interrogativa.</p>
        <p class="paragraph-tex">Na forma afirmativa, o Present Simple dos verbos nada mais é do que o infinitivo do verbo sem a partícula to . Veja:</p>
        <ul>
            <li><strong>I guess</strong> not, but in some parts of the country <strong>people speak</strong> French, not English.</li>
            <li><strong>I</strong> usually <strong>get by</strong> with a dictionary. <strong>People</strong> often <strong>try</strong> to help you when <strong>they see</strong> you’re trying to communicate in their language.</li>
            <ul>
                <li>And <strong>I</strong> always <strong>hear</strong> people say that the Canadian people are very nice and friendly, so <strong>I think</strong> there’s a good chance I can have a great trip.</li>
            </ul>
        </ul>
        <p class="paragraph-tex">Nesses exemplos não aparece, entretanto, nenhuma frase com as pessoas he, she ou it, nas quais o verbo sofre uma pequena modificação: ele é acrescido de -s, -es ou -ies, com exceção do verbo To have que muda para Has. Veja as frases abaixo:</p>
        <ul>
            <li>Carlton relaxes for five minutes before his night shift.</li>
            <li>Dube plays video games in his free time.</li>
            <li>Rosa studies at the Columbus College of Chicago.</li>
        </ul>
        <p class="paragraph-tex">Nas frases negativas, utilizamos o seguinte auxiliar:</p>
        <ul>
            <li>do + not = don’t.</li>
            <li>does + not = doesn’t</li>
        </ul>
        <p class="paragraph-tex">Observe o uso da forma negative nas frases a seguir:</p>
        <ul>
            <li>I <strong>don’t speak</strong> French fluently.</li>
            <li>I <strong>don’t have</strong> any idea where.</li>
        </ul>
        <p class="paragraph-tex">Se essas frases fossem a respeito de ou sobre Rosa, elas seriam:</p>
        <ul>
            <li>She <strong>doesn’t speak</strong> French fluently.</li>
            <li>She <strong>doesn’t have</strong> any idea where.</li>
        </ul>
        <p class="paragraph-tex">Nas perguntas, os auxiliares do e does também aparecem posicionados na frente do sujeito. Por exemplo:</p>
        <ul>
            <li>Where <strong>do</strong> you stay when you travel?</li>
            <li>How <strong>do</strong> you travel when you visit your family?</li>
            <li>What <strong>does</strong> she wear on plane trips?</li>
            <li>Why <strong>does</strong> he pack so many clothes?</li>
        </ul>
