                    </tr>
                </tbody>
            </table>
        </div>
        <p class="paragraph-tex">Para formar corretamente os verbos no Present Continuous, lembre-se de que, se o verbo terminar em “ie”, essas letras serão substituídas por “y” antes do acréscimo do -ing. Exemplos:</p>
