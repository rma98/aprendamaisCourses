        <p class="paragraph-tex">Outra maneira de expressar a posse é o possessive case. Neste caso, o possuidor recebe o 's no final, e aquilo que é possuído vem logo a seguir. Assim, se você for mencionar uma viagem que Rosa fará ou fez, basta dizer Rosa’s trip.</p>
