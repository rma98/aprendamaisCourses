        <p class="paragraph-tex">Depois de relembrar como conjugar os verbos no Present Continuous, acompanhe, no tópico a seguir, alguns Modal Verbs.</p>

        <h3 class="sub-heading">3.10 Can and may</h3>
        <p class="paragraph-tex">Can e May pertencem a um grupo de verbos conhecido como Modal Verbs. Esses verbos não variam em função do sujeito com que são utilizados e, tampouco, o verbo que os acompanha muda ou sofre qualquer acréscimo. Observe os exemplos do diálogo entre Rosa e Carly:</p>
        <ul>
            <li>I <strong>can have</strong> a great trip.</li>
            <li>You <strong>can get</strong> an interpreter.</li>
            <li>I <strong>can’t understand</strong> him at all.</li>
            <li><strong>May</strong> I <strong>suggest</strong> something?</li>
            <li>I <strong>may be</strong> wrong.</li>
        </ul>
        <h3 class="sub-heading">Can</h3>
        <p class="paragraph-tex">Primeiramente, vamos falar de Can, que pode indicar:</p>
        <ul>
            <li>Habilidade ou falta dela, como na fala de Carly:</li>
            <ul>
                <li>I <strong>can’t understand</strong> him at all.</li>
            </ul>
        </ul>
        <p class="paragraph-tex">Ou seja, ela não pode ou não consegue entender Carlton de jeito nenhum.</p>
        <ul>
            <li>Possibilidade, como na fala de Rosa:</li>
            <ul>
                <li>I <strong>can have</strong> a great trip.</li>
            </ul>
        </ul>
        <p class="paragraph-tex">Nessa frase, é possível que ela tenha uma ótima viagem.</p>
        <ul>
            <li>Outro exemplo é quando Carly brinca com Rosa sobre a possibilidade de ela conseguir um intérprete:</li>
            <ul>
                <li>You <strong>can get</strong> an interpreter.</li>
            </ul>
        </ul>
        <h3 class="sub-heading">May</h3>
        <p class="paragraph-tex">O verbo modal May, apesar de também significar poder, é utilizado para:</p>
        <ul>
            <li>Pedir ou dar permissão, como quando Carly pergunta a Rosa:</li>
            <ul>
                <li><strong>May I suggest</strong> something?</li>
            </ul>
        </ul>
        <p class="paragraph-tex">Nessa frase Carly pede permissão para sugerir algo à amiga.</p>
        <ul>
            <li>Indicar possibilidade, como quando Rosa diz:</li>
            <ul>
                <li>I <strong>may be</strong> wrong.</li>
            </ul>
        </ul>
        <p class="paragraph-tex">Nessa frase, Rosa refere-se ao fato de que pode estar errada quanto a Carlton estar indo para uma cidade no Canadá próxima à fronteira com os Estados Unidos.</p>
        <p class="paragraph-tex">A seguir, revise o uso dos pronomes oblíquos que te permitirão referir-se a terceiros.</p>

