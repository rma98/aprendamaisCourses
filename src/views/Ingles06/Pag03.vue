        <ul>
            <li>Her friends’ trip was fantastic.</li>
        </ul>
        <p class="paragraph-tex">Quando houver um substantivo irregular no plural, como por exemplo, children, acrescenta-se ’s:</p>
        <ul>
            <li>The children’<strong>s</strong> seats are by the window.</li>
        </ul>
        <p class="paragraph-tex">Nomes próprios terminados em “s”, como por exemplo, Chris ou Janis, acrescenta-se o ’s:</p>
        <ul>
            <li>Chris’<strong>s</strong> suitcase is old.</li>
            <li>Janis’<strong>s</strong> plane is big.</li>
        </ul>
        <p class="paragraph-tex">E, por fim, quando você precisar mencionar vários donos, terá que analisar a situação. Observe:</p>
        <ul>
            <li>John, Mary and Pete’s flight is early. (o voo é dos três ao mesmo tempo).</li>
            <li>John’s, Mary’s and Pete’s flights are early. (cada um tem um voo).</li>
        </ul>
