        <h3 class="sub-heading">3.5 Talking about possession</h3>
        <p class="paragraph-tex">Em inglês existe mais de uma possibilidade para expressar a posse de algo. Uma delas é por meio do uso dos possessive adjectives, a outra é por meio dos possessive pronouns.</p>
        <p class="paragraph-tex">Os possessive adjectives, como o próprio nome diz, são adjetivos e, por isso, são sempre seguidos de um substantivo. Observe que, ao contrário dos possessive adjectives, os possessive pronouns não são seguidos de substantivo. Veja na tabela:</p>
