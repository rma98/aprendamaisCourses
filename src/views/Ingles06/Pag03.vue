        </div>
        <p class="paragraph-tex">No tópico a seguir, você irá revisar o uso do verbo There to be para indicar a existência de algo.</p>

        <h3 class="sub-heading">3.7 There to be</h3>
        <p class="paragraph-tex">O verbo There to be significa haver ou existir. No entanto, não é necessário que exista algo físico, concreto. Veja as frases no diálogo entre Rosa e Carly:</p>
        <div class="content-table">
