        <p class="paragraph-tex">Lembre-se que, para expressar posse utilizando o verbo ter, utiliza-se o verbo To have.</p>
        <ul>
            <li>I can have a great trip.</li>
            <li>They have fantastic stuff there.</li>
        </ul>
        <p class="paragraph-tex">O verbo There to be pode ser utilizado junto com substantivos contáveis e incontáveis. No tópico seguinte, acompanhe o uso de some e any com estes substantivos.</p>

        <h3 class="sub-heading">3.8 Some, Any and Countable/Uncountable Nouns</h3>
        <p class="paragraph-tex">Ao utilizar o verbo There to be é preciso considerar que estamos lidando com itens no singular ou no plural, o que nos leva à questão dos countable ou uncountable nouns. Os countable nouns são aqueles substantivos que podem ser contados em unidades e, portanto, podem ser colocados e utilizados no plural. Já os uncountable nouns só podem ser utilizados no singular. Observe este trecho do diálogo de Rosa e Carly:</p>
        <ul>
            <li><strong>Carly:</strong> Don’t pack too <u>many clothes</u>. Leave some space to buy some winter gear at the shops in Montreal. They have fantastic  stuff there. Oh, and don’t take too much luggage!</li>
            <li><strong>Rosa:</strong> Thanks! Those are <u>great tips</u>!</li>
        </ul>
        <p class="paragraph-tex">Na Conversa entre Carly e Rosa, as amigas usam os termos clothes e tips. Tanto roupas quanto dicas são contáveis e, portanto, são countable nouns. Já quando Carly usa o termo luggage (bagagem), ela se refere a algo que não pode ser contado em unidades, logo, é um uncountable noun.</p>
        <p class="paragraph-tex">Para perguntar quanto há de alguma coisa contável, utilizamos how many:</p>
        <ul>
            <li><strong>How many</strong> bags are you taking on your trip?</li>
        </ul>
        <p class="paragraph-tex">E se for incontável, utilizamos how much:</p>
        <ul>
            <li><strong>How much</strong> luggage are you taking on your trip?</li>
        </ul>
        <p class="paragraph-tex">Quando queremos dizer que há muito de alguma coisa, utilizamos a lot of ou lots of, que podem ser usadas tanto para countable quanto para uncountable nouns.</p>
        <ul>
            <li>There’s <strong>a lot of</strong> stuff in your bags!</li>
            <li>There are <strong>lots of bags</strong> for you to carry on your trip.</li>
        </ul>
        <p class="paragraph-tex">Já para dizer que há algo em excesso, enfatizar que a quantidade é maior do que o necessário, utiliza-se too com many e much, que são utilizados respectivamente com countable nouns e uncountable nouns. Observe o uso  de many e much nas frases que seguem:</p>
        <ul>
            <li><strong>Don’t</strong> pack too <strong>many clothes</strong>.</li>
            <li>Oh, and <strong>don’t</strong> take too <strong>much luggage</strong>.</li>
        </ul>
        <p class="paragraph-tex">Para dizer que não há nada de alguma coisa, utilizamos any, que pode ser usado tanto com countable quanto uncountable nouns. Acompanhe algumas situações de uso:</p>
        <p class="paragraph-tex">Nas frases negativas, any significa nenhum/nenhuma:</p>
        <ul>
            <li>There isn’t <strong>any</strong> problem with my visa.</li>
            <li>There aren’t <strong>any</strong> plane tickets to Toronto.</li>
        </ul>
        <p class="paragraph-tex">Nas frases interrogativas, any assume o significado de algum(s)/alguma(s) com o sentido de qualquer um ou qualquer quantidade:</p>
        <ul>
            <li><strong>Is there any</strong> plane ticket to Toronto?</li>
            <li><strong>Are there any</strong> winter clothes in your luggage?</li>
        </ul>
        <p class="paragraph-tex">Também podemos expressar quantidade utilizando some. Ele é utilizado em frases afirmativas, tanto com countable quanto com uncountable nouns. Some significa algum(s)/alguma(s) no sentido de uma quantidade indefinida. Acompanhe o que Carly diz a Rosa:</p>
        <ul>
            <li>Leave <strong>some space</strong> to buy <strong>some winter gear</strong> at the shops in Montreal.</li>
            <li>Many times people are just shy and <strong>some</strong> are simply not very talkative.</li>
        </ul>
        <p class="paragraph-tex">Na frase acima, Carly se refere a uma quantidade indefinida de espaço na mala de Rosa (some space = algum espaço), assim como a uma quantidade indefinida de equipamento de inverno (some winter gear = alguns equipamentos de inverno). Nesses dois casos, space e winter gear são uncountable nouns, mas quando Rosa diz a Carly que "some [people] are simply not very talkative", ela está referindo-se a uma quantidade indefinida de pessoas, que é um countable noun.</p>
        <p class="paragraph-tex">No tópico a seguir você irá revisar o Present Continuous para falar sobre ações em andamento.</p>

        <h3 class="sub-heading">3.9 Present Continuous</h3>
        <p class="paragraph-tex">Outro tempo verbal utilizado para se falar sobre o presente é o Present Continuous. Diferentemente do Present Simple, utilizado para expressar hábitos, rotinas e ações que ocorrem com regularidade, o Present Continuous expressa ações que estão ocorrendo no momento da fala ou em andamento e, por essa razão, frequentemente vem acompanhado de expressões como now, at the moment, currently , etc.</p>
        <p class="paragraph-tex">Quando Rosa diz a Carly “I’m still putting it all <strong>together</strong>”, quer dizer que a ação de reunir as coisas para a viagem ainda está em andamento enquanto ela conversa com a amiga, mas a ação ainda não foi concluída.</p>
        <p class="paragraph-tex">É possível identificar um verbo no Present Continuous pela terminação -ing adicionada ao verbo principal. Leia alguns exemplos retirados da conversa entre as garotas:</p>
