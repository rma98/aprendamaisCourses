        <h3 class="sub-heading">3.14 Comparative and superlative</h3>
        <p class="paragraph-tex">Quando comparamos duas coisas, lugares ou pessoas, utilizamos a comparative form. No diálogo entre Rosa e Carly você pode ver um exemplo da comparative form quando elas falam de Carlton: "He’s more secretive than any person I know".</p>
        <p class="paragraph-tex">Conforme o exemplo, podemos observar que Carly compara Carlton a outra pessoa, ou melhor, a qualquer pessoa que ela conhece. Nesse caso, quando fazemos comparações, o primeiro aspecto a se considerar é se o adjetivo é short, long ou irregular. Acompanhe os exemplos:</p>
        <ul>
            <li>Billy is old<strong>er than</strong> Carly.</li>
            <li>Carly is young<strong>er than</strong> Rosa.</li>
            <li>Carly is <strong>more</strong> talkative <strong>than</strong> Carlton.</li>
            <li>Dube is <strong>more</strong> enthusiastic <strong>than</strong> Christine.</li>
            <li>Traffic at night is <strong>better than</strong> traffic in the morning.</li>
            <li>Rosa plays basketball <strong>worse than</strong> Carly.</li>
        </ul>
        <p class="paragraph-tex">Para as formas superlativas, ou seja, quando você quer colocar alguma coisa, lugar ou pessoa em posição de destaque, usamos o mesmo raciocínio utilizado para construir frases na forma comparativa. A diferença é que a partícula than não está mais presente, e em vez de -er teremos -est, assim como no lugar de more teremos most. Observe as frases e perceba que o the sempre acompanha os superlatives:</p>
        <ul>
            <li>Dube is <strong>the</strong> old<strong>est</strong> in the group.</li>
            <li>Carly is <strong>the</strong> young<strong>est</strong> of all.</li>
            <li>Carlton is <strong>the most</strong> secretive person they know.</li>
            <li>Carly is <strong>the most</strong> competitive girl in the group.</li>
            <li>Dube is <strong>the best</strong> stand-up comedian at the pub.</li>
            <li>Midday is <strong>the worst</strong> time to catch a bus.</li>
        </ul>
        <p class="paragraph-tex">Revisadas as Comparative and Superlative form, acompanhe o uso do Future: Going to no tópico a seguir.</p>

        <h3 class="sub-heading">3.15 Future: Going to</h3>
        <p class="paragraph-tex">Para expressar uma ação planejada ou uma intenção no futuro utiliza-se Going to, acompanhado pelo Verb To be que funciona como verbo auxiliar (be going to). Veja os exemplos:</p>
        <ul>
            <li>OK, now tell me the truth: <strong>are you gonna tell</strong> your Brazilian friends that you had a nice time here in Chicago?</li>
            <li>And when <strong>are you going to leave</strong>?</li>
            <li>Would you like my help to pack? No, thanks. That’s OK. I’<strong>m going to do</strong> it tonight.</li>
        </ul>
        <p class="paragraph-tex">As frases que você acabou de conferir tratam de intenções ou planos, por isso utilizam o Going to. As frases negativas seguem a mesma lógica de construção, apenas acrescentando a partícula not. Veja alguns exemplos:</p>
        <ul>
            <li>Carly and Billy <strong>are not going to</strong> travel.</li>
            <li>Rosa’<strong>s not going to spend</strong> Christmas with her parents.</li>
        </ul>
        <p class="paragraph-tex">As short answers do Going to - Future também utilizam o verbo To be na sua composição:</p>
